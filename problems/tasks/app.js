_input = '';

process.stdin.resume();

process.stdin.setEncoding("ascii");

function XyloHack (str , n) {
	if(n % 2)
		return str.toLowerCase();

	return str.toUpperCase();
};

function processData () {
	_input = _input.split('\n');
	var len = _input[0];
	for(var i = 0 ; i < len ; i++){
		var argumentStr = _input[i+1].split('.XyloHack')[1];
		var inputStr = _input[i+1].split('.XyloHack')[0];
		var argument = argumentStr.substring(1 , argumentStr.length - 1);
		console.log(XyloHack(inputStr, argument));
	}
}

process.stdin.on("data", function (input) {
	_input += input;
});

process.stdin.on("end", function () {
	processData();
});

